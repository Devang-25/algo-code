    public int search0(int[] nums, int target) {

        if (nums.length > 0) {

            int pivot = findPivot(nums);
            int leftFind = Arrays.binarySearch(nums, 0, pivot + 1, target);
            int rightFind = Arrays.binarySearch(nums, pivot + 1, nums.length, target);

            //System.out.println(pivot + " " + leftFind + " " + rightFind);
            int out = leftFind < 0 ? rightFind : leftFind;
            return out < 0 ? -1 : out;
        }
        return -1;
    }

